func SignOut(c *fiber.Ctx) error {
	c.Cookie(&fiber.Cookie{
		Name:     "jwt",
		Value:    "",
		Expires:  time.Now().Add(-1 * time.Hour), 
		HTTPOnly: true,
	})
	return c.Status(http.StatusOK).JSON(fiber.Map{"message": "Successfully signed out"})
}